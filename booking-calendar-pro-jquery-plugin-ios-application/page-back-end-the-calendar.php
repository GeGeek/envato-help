                    <h6>Availability form</h6>
                    <ul>
                        <li><strong>Button 1</strong> - Submit form data. Click on it to set availability.</li>
                        <li><strong>Button 2</strong> - Reset (remove) selected period data. Click on it to remove availability.</li>
                        <li><strong>Item 3</strong> - The first day of the period selected to set availability.</li>
                        <li><strong>Item 4</strong> - The last day of the period selected to set availability.</li>
                        <li><strong>Item 5</strong> - Set selected days status.</li>
                        <li><strong>Item 6</strong> - Set selected days price.</li>
                        <li><strong>Item 7</strong> - Set selected days promotional price.</li>
                        <li><strong>Item 8</strong> - Set selected days number of items available. Leave it blank to not display any number. The default value is 1.</li>
                        <li><strong>Item 9</strong> - Set selected days information, which will be visible to the users.</li>
                        <li><strong>Item 10</strong> - Set selected days notes, which will be visible only to the administrators.</li>
                        <li><strong>Item 11</strong> - Group selected days into a single block. All data will be attributed to it.</li>
                    </ul>
