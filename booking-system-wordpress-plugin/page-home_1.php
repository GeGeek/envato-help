                        <li>
                            <p class="question">
                                7. Why does not the calendar appear on my website?
                            </p>
                            <p class="answer">
                                1. If the calendar does not appear, it might be because there is a problem with the JavaScript in your website. If you cannot identify the problem, <a href="http://envato-support.dotonpaper.net/" target="_blank">contact us</a>, including in the message a link to the page where the problem appears. We will identify and fix the issue for you. However, we will not fix the problems that are not caused by this plugin.
                                <br /><br />
                                2. Another reason might be that you load more than one jQuery file into your theme. The proper way to load jQuery into your theme or plugin is the following (your add it into your theme's header):
                                <br /><br />
                                <script type="syntaxhighlighter" class="brush: js; toolbar: false;">
                                <![CDATA[
                                    if (!wp_script_is('jquery', 'queue')){
                                         wp_enqueue_script('jquery');
                                    }
                                ]]>
                                </script>
                            </p>
                        </li>
