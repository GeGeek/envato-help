                                6. How do I change the language?
                            </p>
                            <p class="answer">
                                For the Front End you add the attribute <strong>lang</strong> in the shortcode:<br />
                                <strong>[dopbsp id=1 lang=en]</strong>.<br />
                                The <strong>lang</strong> value is a 2 character code representing the language.
                                <br /><br />
                                For the Back End you select the language from the top-right corner.<br />
                            </p>
