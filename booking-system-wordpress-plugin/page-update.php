                    <h2>How to update</h2>
                    
                    <p>
                        All updates are free and you can download them <a href="http://codecanyon.net/downloads" target="_blank">here</a>.
                    </p>
                    
                    <h6>Latest Version: 2.0.2</h6>
                    
                    <h6>Important!</h6>
                    <ul>
                        <li>Make a <strong>backup</strong> of the Plugin inside your WordPress installation folder <strong>/wp-content/plugins/dopbsp/</strong>.</li>
                    </ul>
                    
                    <h6>WARNING! If you update from 1.x to 2.x</h6>
                    <ul>
                        <li>Change admin language back to the default one (English).</li>
                        <li>Do not delete the old files because it is possible to lose some of your data!</li>
                        <li>If you intend to update from an older version most of the translation you added will be lost.</li>
                        <li>Permissions to users to use the booking system needs to be given again. No data is lost, but they will not see the calendars because they are not allowed to.</li>
                        <li>The way you add the booking system to a WooCommerce product has changed. You will need to configure them again.</li>
                        <li>You need to setup user permissions for specific calendars again.</li>
                    </ul>
                    
                    <h6>Update</h6>
                    <ul>
                        <li>To update the Plugin, login to <a href="http://www.codecanyon.net/?ref=DOTonPAPER">CodeCanyon</a>, head over to your <strong>Downloads</strong> section and re-download the plugin like you did when you bought it.</li>
                        <li>
                            Extract the zip's contents, look for the extracted plugin folder, and after you have all the new files upload them using FTP to the <strong>/wp-content/plugins/dopbsp/</strong> folder overwriting the old ones (this is why it's important to backup any changes you've made to the plugin files).
                            <br /><br />
                            If you didn't make any changes to the plugin files, you are free to overwrite them with the new ones without the risk of losing any plugins settings, and backwards compatibility is guaranteed.
                        </li>
                    </ul>
